class Matematica {
  static somar(valor1, valor2) {
    return Number(valor1) + Number(valor2)
  }

  static multiplicar(valor1, valor2) {
    return Number(valor1) * Number(valor2)
  }

  static subtrair(valor1, valor2) {
    return Number(valor1) - Number(valor2)
  }

  static dividir(valor1, valor2) {
    return Number(valor1) / Number(valor2)
  }
}

